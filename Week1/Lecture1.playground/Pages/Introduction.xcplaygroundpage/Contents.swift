 ## Swift Language

 Swift is a general-purpose programming language that’s approachable for newcomers and powerful for experts.
 It is fast, modern, safe, and a joy to write [[1]].
 
- Swift is general-purpose and modern.  Systems programming, through mobile and desktop apps, to cloud services.
- Swift is safe. Undefined behavior is the enemy of safety, and it’s best to catch mistakes before software goes into production.
- Swift is fast to run and quick to write.
- Swift is approachable and powerful. Single-line “Hello, World!” to large-scale apps with hundreds of thousands of lines.

 
